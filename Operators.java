public class Operators {
    public static void main(String[] args) {
        int x = 7, y = 3;
        System.out.println(x + y);
        System.out.println(x - y);
        System.out.println(x * y);
        System.out.println(x / y);
        System.out.println(x % y);
        System.out.println(x > y);
        System.out.println(x == y);
        System.out.println((x > 5) && (y < 5));
        System.out.println(x | y);
        System.out.println(x >> 1);
    }
}
