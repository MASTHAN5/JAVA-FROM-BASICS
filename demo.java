//if and else statement
public class demo {
    public static void main(String[] args)
    {
        boolean rain = false; //store true or false in boolean to get the decision
        if(rain){
            System.out.println("Take an umbrella"); //scenario 1
        }
        else{
            System.out.println("Enjoy the sunlight"); //scenario 2
        }
    }
    
}

